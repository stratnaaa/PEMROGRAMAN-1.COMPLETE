#include <stdio.h>
int main(){
    float pyz, hr;
    pyz = 958.730; //Pasukan Yu Zhong
    hr = 5; //Jumlah pahlawan yang mengalahkan Pasukan Yu Zhong
    printf("Jumlah pasukan yang dibawa Yu Zhong = %.3f\n", pyz);
    printf("Jumlah pahlawan = %.f\n", hr);
    printf("Jumlah pasukan yang harus dikalahkan setiap pahlawan adalah %.3f Pasukan",pyz / hr);
    return 0;

}