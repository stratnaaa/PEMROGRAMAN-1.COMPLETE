#Putaran mengelilingi taman
pt = 5
#Jarak tempuh
jt = 14
#Keliling taman
kt = jt / pt
print("Diketahui :")
print("Pak Dengklek mengelilingi taman =",pt, "Putaran")
print("Jarak tempuh Pak Dengklek =",jt, "Kilometer")
print("\n" * 2) #new line blank
print("Jawaban :")
print("Jari-jari taman yang dikelilingi Pak Dengklek adalah", round((kt)/(2*3.14), 2), "Kilometer")